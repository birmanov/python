# Шаг 1: Создайте два списка
list1 = [1, 2, 3]
list2 = [4, 5, 6]

# Шаг 2: Объедините два списка, используя оператор +
merged_list = list1 + list2

# Шаг 3: Определите, какой магический метод списков вызывается при использовании оператора +
# Магический метод __add__() вызывается при использовании оператора +
# на объектах класса списка (list)

# Шаг 4: Выполните слияние списков, используя магический метод __add__()
merged_list = list1.__add__(list2)

# Шаг 5: Выведите результат в терминал
print(merged_list)
